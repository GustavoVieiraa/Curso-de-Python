# Escreva um programa que leia um número n inteiro qualquer
# e mostre na tela os n primeiros elemento de uma Sequência de Fibonacci.
# ex. 0 -> 1 -> 1 -> 2 -> 3 -> 5 -> 8


print('=-' * 5 + ' Sequência de Fibonacci ' + '-=' * 5)

c = 1

while c < 11:
    print(c)
    c += 1
print('=-' * 5 + ' Sequência de Fibonacci ' + '-=' * 5)
